#include <iostream>  
using namespace std;

int main()   
{  
     int a = 13;           
     unsigned int b = 28;      
     short int c = 32747;       
     long int  d = 1000000;  
     cout << "The value of a is: " << a << " " << sizeof(a) << endl;  
     cout << "The value of b is: " << b << " " << sizeof(b) << endl;  
     cout << "The value of c is: " << c << " " << sizeof(c) << endl;  
     cout << "The value of d is: " << d << " " << sizeof(d) << endl;  
     return 0;  
}  
